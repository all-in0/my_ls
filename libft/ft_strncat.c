#include <string.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int i;
	int b;

	i = 0;
	b = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[b] != '\0' && n != 0)
	{
		s1[i] = s2[b];
		i++;
		b++;
		n--;
	}
	s1[i] = '\0';
	return (s1);
}
