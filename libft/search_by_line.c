#include "ft_printf.h"

int		search_by_line(char *s1, char *s2)
{
	int		i;
	int		b;

	i = -1;
	while (s1[++i] != '\n' && s1[i] != '\0')
	{
		b = 0;
		while (s2[b] == s1[i + b])
		{
			if (s2[b + 1] == '\0')
				return (1);
			b++;
		}
	}
	return (0);
}
