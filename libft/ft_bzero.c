#include <string.h>

void	ft_bzero(void *str, size_t n)
{
	size_t			i;
	unsigned char	*q;

	i = 0;
	q = (unsigned char *)str;
	while (n > i)
	{
		q[i] = 0;
		i++;
	}
}
